<script>
import { onMounted } from 'vue'
import { useRoute } from 'vue-router'
import { useLeagueDetail } from '../composables/useLeagueDetail.js'
import { useDateFormatter } from '../composables/useDateFormatter.js'

export default {
  name: 'LeagueDetailView',
  setup() {
    const route = useRoute()
    const leagueId = route.params.id
    const { formatEventDate } = useDateFormatter()
    
    // Usar el composable con la l√≥gica
    const {
      // Estados
      currentLeague,
      currentEvent,
      globalLeaderboard,
      currentEventLeaderboard,
      currentUserPicks,
      myPosition,
      
      // Estados de carga
      isLoadingLeague,
      isLoadingGlobal,
      isLoadingCurrentEvent,
      
      // Estados UI
      activeTab,
      selectedFighter,
      showLeagueInfo,
      showNotificationModal,
      notificationText,
      
      // Computed
      isPublicLeague,
      isPrivateLeague,
      canMakePicks,
      formattedEventDate,
      
      // Funciones
      loadAllData,
      refreshTabData,
      getPositionClass,
      getFighterInitials,
      showFighterDetails,
      hideNotification,
      goBackToDashboard,
      goToPicksSelection
    } = useLeagueDetail(leagueId)

    // Funciones adicionales del componente
    const formatDate = (dateString) => {
      if (!dateString) return 'N/A'
      try {
        const date = new Date(dateString)
        return date.toLocaleDateString('es-ES', {
          year: 'numeric',
          month: 'long',
          day: 'numeric'
        })
      } catch (error) {
        return 'N/A'
      }
    }

    const formatCurrency = (amount) => {
      if (!amount) return '0'
      return new Intl.NumberFormat('es-ES').format(amount)
    }

    const getEventStatusClass = (status) => {
      switch(status) {
        case 'UPCOMING': return 'upcoming'
        case 'LIVE': 
        case 'IN_PROGRESS': return 'current'
        case 'COMPLETED': return 'completed'
        default: return 'upcoming'
      }
    }

    const getEventStatusText = (status) => {
      switch(status) {
        case 'UPCOMING': return '‚è≥ Pr√≥ximamente'
        case 'LIVE': 
        case 'IN_PROGRESS': return 'üî¥ En Vivo'
        case 'COMPLETED': return '‚úÖ Finalizado'
        default: return '‚è≥ Pr√≥ximamente'
      }
    }

    const setActiveTab = async (tabName) => {
      if (activeTab.value === tabName) return
      
      activeTab.value = tabName
      
      // Cargar datos de la pesta√±a si es necesario
      await refreshTabData(tabName)
    }

    // Cargar todos los datos al montar el componente
    onMounted(async () => {
      console.log('üöÄ Montando LeagueDetailView para liga:', leagueId)
      
      if (!leagueId) {
        console.error('‚ùå No se proporcion√≥ ID de liga')
        goBackToDashboard()
        return
      }
      
      try {
        await loadAllData()
      } catch (error) {
        console.error('üí• Error cr√≠tico al cargar la liga:', error)
      }
    })

    return {
      // Estados del composable
      currentLeague,
      currentEvent,
      globalLeaderboard,
      currentEventLeaderboard,
      currentUserPicks,
      myPosition,
      
      // Estados de carga
      isLoadingLeague,
      isLoadingGlobal,
      isLoadingCurrentEvent,
      
      // Estados UI
      activeTab,
      selectedFighter,
      showLeagueInfo,
      showNotificationModal,
      notificationText,
      
      // Computed
      isPublicLeague,
      isPrivateLeague,
      canMakePicks,
      formattedEventDate,
      
      // Funciones del composable
      loadAllData,
      getPositionClass,
      getFighterInitials,
      showFighterDetails,
      hideNotification,
      goBackToDashboard,
      goToPicksSelection,
      
      // Funciones del componente
      formatDate,
      formatCurrency,
      getEventStatusClass,
      getEventStatusText,
      setActiveTab
    }
  }
}
</script>

